
public class Car {
	
	private String model;
	private String color;
	
	public Car(String model, String color) {
		super();
		this.model = model;
		this.color = color;
	}
	
	public void printCarDetails() {
		
		System.out.println("Car mode is: "+this.model);
		System.out.println("Car mode is: "+this.color);
		
	}
	
	

}
